"use strict";

const fs = require("node:fs");
const path = require("node:path");
const vm = require("node:vm");

const root = path.resolve(__dirname, "..");
const appPath = path.join(root, "app.js");
const htmlPath = path.join(root, "index.html");
const appSource = fs.readFileSync(appPath, "utf8");
const html = fs.readFileSync(htmlPath, "utf8");

function extract(pattern, label) {
  const match = appSource.match(pattern);
  if (!match) throw new Error(`Could not extract ${label} from app.js`);
  return match[1];
}

const appsSource = extract(/const APPS = (\[[\s\S]*?\n\]);/, "APPS");
const glyphSource = extract(/(function glyphHTML\(app\) \{[\s\S]*?\n\})/, "glyphHTML");
const detailSource = extract(/(function detailHTML\(app\) \{[\s\S]*?\n\})/, "detailHTML");
const { APPS, detailHTML } = vm.runInNewContext(
  `const APPS = ${appsSource};\n${glyphSource}\n${detailSource}\n({ APPS, detailHTML });`,
  Object.create(null),
);

const generated = [
  "    <div id=\"projects-fallback\" class=\"fallback-grid\">",
  "      <!-- generated by scripts/generate-project-fallback.js; do not edit -->",
  ...APPS.map((app) => `      <article class=\"app-detail project-detail\">${detailHTML(app)}\n      </article>`),
  "    </div>",
].join("\n");

const fallbackPattern = /    <div id="projects-fallback" class="fallback-grid">[\s\S]*?\n    <div class="collage reveal"/;
const nextHtml = html.replace(fallbackPattern, `${generated}\n    <div class="collage reveal"`);
if (nextHtml === html && !html.includes(generated)) throw new Error("Could not locate #projects-fallback in index.html");

if (process.argv.includes("--check")) {
  if (nextHtml !== html) {
    console.error("index.html project fallback is out of sync; run node scripts/generate-project-fallback.js");
    process.exitCode = 1;
  }
} else {
  fs.writeFileSync(htmlPath, nextHtml);
}
